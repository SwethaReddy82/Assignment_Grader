# ── Individual feedback files ─────────────────────────────────────────────────

def write_student_feedback(result: Dict, output_dir: str) -> str:
    """
    Write a detailed plain-text feedback file for a single student.
    Returns the path to the created file.
    """
    Path(output_dir).mkdir(parents=True, exist_ok=True)
    safe_name = _safe_filename(result["student_name"])
    file_path = os.path.join(output_dir, f"{safe_name}_feedback.txt")

    lines = []
    _line = lines.append

    _line("=" * 70)
    _line(f"  ASSIGNMENT FEEDBACK — AI FOR ANALYTICS")
    _line("=" * 70)
    _line(f"  Student : {result['student_name']}")
    _line(f"  File    : {result.get('file', 'N/A')}")
    _line(f"  Date    : {datetime.now().strftime('%B %d, %Y')}")
    _line("=" * 70)
    _line("")

    # Score summary
    _line("SCORE SUMMARY")
    _line("-" * 40)
    total = result.get("total_score", 0)
    max_s = result.get("max_score", 100)
    pct   = result.get("percentage", 0.0)
    grade = result.get("letter_grade", "N/A")
    _line(f"  Total Score  : {total} / {max_s} points")
    _line(f"  Percentage   : {pct}%")
    _line(f"  Letter Grade : {grade}")
    _line("")

    # Overall summary
    _line("OVERALL FEEDBACK")
    _line("-" * 40)
    _line(_wrap(result.get("overall_summary", ""), width=68))
    _line("")

    # Per-criterion breakdown
    criteria = result.get("criteria", [])
    if criteria:
        _line("CRITERION BREAKDOWN")
        _line("-" * 40)
        for c in criteria:
            awarded = c.get("awarded_points", 0)
            maximum = c.get("max_points", 0)
            _line(f"  [{awarded}/{maximum}]  {c.get('name', 'Unknown')}")
            feedback_text = c.get("feedback", "")
            if feedback_text:
                for wrapped_line in _wrap(feedback_text, width=66, indent="        ").split("\n"):
                    _line(wrapped_line)
            _line("")

    # Strengths
    strengths = result.get("strengths", [])
    if strengths:
        _line("STRENGTHS")
        _line("-" * 40)
        for s in strengths:
            _line(f"  ✓  {s}")
        _line("")

    # Areas for improvement
    areas = result.get("areas_for_improvement", [])
    if areas:
        _line("AREAS FOR IMPROVEMENT")
        _line("-" * 40)
        for a in areas:
            _line(f"  •  {a}")
        _line("")

    # Actionable suggestions
    suggestions = result.get("actionable_suggestions", "")
    if suggestions:
        _line("ACTIONABLE SUGGESTIONS")
        _line("-" * 40)
        _line(_wrap(suggestions, width=68))
        _line("")

    _line("=" * 70)
    _line("  Generated by AI Grading Assistant — AI for Analytics Course")
    _line("=" * 70)

    with open(file_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))

    return file_path


# ── CSV summary ───────────────────────────────────────────────────────────────

def write_csv_summary(results: List[Dict], output_dir: str) -> str:
    """
    Write a CSV summary with one row per student.
    Returns the path to the created file.
    """
    Path(output_dir).mkdir(parents=True, exist_ok=True)
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    file_path = os.path.join(output_dir, f"grading_summary_{timestamp}.csv")

    # Collect all unique criterion names across all results
    all_criteria: List[str] = []
    for r in results:
        for c in r.get("criteria", []):
            name = c.get("name", "")
            if name and name not in all_criteria:
                all_criteria.append(name)

    # Build CSV header
    base_fields = [
        "Student Name",
        "Submission File",
        "Total Score",
        "Max Score",
        "Percentage (%)",
        "Letter Grade",
    ]
    # Add one column per criterion: "Criterion Name (score)"
    criterion_fields = [f"{c} (pts)" for c in all_criteria]
    header = base_fields + criterion_fields + ["Overall Summary", "Strengths", "Areas for Improvement"]

    rows = []
    for r in results:
        row = {
            "Student Name": r.get("student_name", ""),
            "Submission File": r.get("file", ""),
            "Total Score": r.get("total_score", 0),
            "Max Score": r.get("max_score", 100),
            "Percentage (%)": r.get("percentage", 0.0),
            "Letter Grade": r.get("letter_grade", "N/A"),
            "Overall Summary": r.get("overall_summary", ""),
            "Strengths": "; ".join(r.get("strengths", [])),
            "Areas for Improvement": "; ".join(r.get("areas_for_improvement", [])),
        }
        # Fill criterion scores
        criteria_lookup = {c.get("name", ""): c for c in r.get("criteria", [])}
        for cname in all_criteria:
            c = criteria_lookup.get(cname, {})
            awarded = c.get("awarded_points", "N/A")
            maximum = c.get("max_points", "")
            row[f"{cname} (pts)"] = f"{awarded}/{maximum}" if maximum else str(awarded)
        rows.append(row)

    with open(file_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=header)
        writer.writeheader()
        writer.writerows(rows)

    return file_path


