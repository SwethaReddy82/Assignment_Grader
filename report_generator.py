# ── Utilities ─────────────────────────────────────────────────────────────────

def _criteria_table(criteria: List[Dict]) -> str:
    rows = ""
    for c in criteria:
        awarded = c.get("awarded_points", "N/A")
        maximum = c.get("max_points", "?")
        pct = round((awarded / maximum) * 100) if isinstance(awarded, (int, float)) and maximum else 0
        color = _grade_color_pct(pct)
        rows += f"""<tr>
          <td><strong>{c.get('name','')}</strong></td>
          <td style="white-space:nowrap;color:{color};font-weight:600">{awarded}/{maximum}</td>
          <td>{c.get('feedback','')}</td>
        </tr>"""
    return f"""<table class="criteria-table">
      <thead><tr><th>Criterion</th><th>Score</th><th>Feedback</th></tr></thead>
      <tbody>{rows}</tbody>
    </table>"""


def _grade_color(letter_grade: str) -> str:
    mapping = {
        "A+": "#38a169", "A": "#38a169", "A-": "#48bb78",
        "B+": "#4299e1", "B": "#4299e1", "B-": "#63b3ed",
        "C+": "#ed8936", "C": "#ed8936", "C-": "#f6ad55",
        "D":  "#e53e3e",
        "F":  "#c53030",
    }
    return mapping.get(letter_grade, "#718096")


def _grade_color_pct(pct: float) -> str:
    if pct >= 90: return "#38a169"
    if pct >= 80: return "#4299e1"
    if pct >= 70: return "#ed8936"
    return "#e53e3e"


def _safe_filename(name: str) -> str:
    import re
    return re.sub(r"[^\w\-]", "_", name).strip("_")


def _wrap(text: str, width: int = 70, indent: str = "") -> str:
    """Simple word-wrap for plain text output."""
    import textwrap
    return textwrap.fill(text, width=width, initial_indent=indent, subsequent_indent=indent)
